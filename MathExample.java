public class MathExample {
    public static void main(String[] args) {
        int i = 20;
        int j = 30;
        int max = Math.max(i, j);
        
        System.out.println("The maximum of " + i + " and " + j + " is: " + max);

        int min = Math.min(i, j);
        
        System.out.println("The minimum of " + i + " and " + j + " is: " + min);

    }
}